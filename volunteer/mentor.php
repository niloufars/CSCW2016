				<H1>CSCW Reviewer Mentor Program</H1>
				<p>The CSCW Reviewer Mentor Program is intended to create an opportunity for students to participate in the CSCW review process while getting guidance from senior reviewers. The idea is that a small pool of students within the CSCW field are matched with CSCW review mentors, who agree to support the student in writing good reviews. This has three purposes. First, PC members who choose a student reviewer within the mentor program will know that the student will do a good review, since the mentor will see to that. Second, students will have the opportunity to get feedback and discuss the reviewing process with the mentor and therefore become a better reviewer. Third, the students will become expert reviewers for future CSCW conferences!</p>
				
				<h6 class="update">Update! (June 5, 2014)</h6>
				<h3>CSCW 2016 Reviewing Mentor Program Participants</h3>
				<p>The final selection of student reviewers and mentor volunteers are done, and inclosed is the <a href="CSCW2015participantskeywords.pdf" target="_blank">complete list</a> of all student reviewers and their mentors. Please use this list when assigning reviewers. Basically the intended way of using the list is as follows. As PC member you are looking for reviewers to a particular paper, you open the list of available reviewers in the PCS system and see how some of possible reviewers fitting the criteria of the paper are not someone you know. Therefore you look at the student reviewer list and properly you will find the name here, and thus you know that the student will be committed to do a great work, as well as getting excellent supervision in doing so. Thus, you can choose to assign the reviewer to your paper.
				</p>
