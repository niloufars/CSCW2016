			<h2>Guidelines &amp; Submission Requirements</h2>
			
			<p>The Doctoral Colloquium is a forum in which Ph.D. students meet and discuss their work with each other and a panel of experienced CSCW researchers and practitioners. The colloquium itself will begin with dinner Saturday night February 27, 2016 and continue all day Sunday February 28, 2016.
			</p>
			
			<p>We welcome applicants from a broad range of disciplines and approaches that inform CSCW, including anthropology, sociology, computer science, cognitive science, organizational studies, design and related fields. We are particularly interested in applications from institutions and groups that have not traditionally been well-represented at past CSCW conferences.
			</p>
			
			<p>Applicants should be Ph.D. students with an already well-established direction of research relevant to CSCW, but whose research would benefit from guidance provided by peers and senior colleagues at the Doctoral Colloquium. Preference will be given to students who have a defined topic and program of work e.g., who have proposed their topics and are within 2 years of graduation within a 5 year program or are half way through a 3 (or 4) year program.
			</p>
			
			<p>The Colloquium committee will select approximately 15 participants who will be expected to give short, informal presentations of their work during the Colloquium, to be followed by extensive group discussion. Participants will also present their research in an interactive poster session during the main technical program of the conference.
			</p>
			
			<p>Applicants should submit the following items by email to cscw<?php echo $year; ?>doctoral@acm.org with the subject line "CSCW<?php echo $year; ?> Colloquium Submission." All items below should be collated into one PDF file named YOURNAME_CSCWDC<?php echo $year; ?>.pdf (substitute your full name where it reads YOURNAME):
				<ul>
					<li>A FOUR-page overview of your doctoral research that describes your research question, work in progress, and expected contributions. This overview should also include (1) a paragraph that articulates what you hope to gain from attending the CSCW Doctoral Colloquium, and (2) an abstract of no more than 100 words. Your overview will be published in the ACM Digital Library and distributed to all attendees as part of the Conference Extended Abstracts. Please submit your overview in the <strong>SIGCHI
					Extended Abstract Format</strong> (<a href="sigchi-word-extended-abstracts.zip">Word</a>, <a href="sigchi-latex-extended-abstracts.tar.gz">Latex</a>).
					</li>
					<li>A short (2-3 paragraph) biographical sketch on another page.</li>
					<li>An up-to-date curriculum vitae.</li>
					<li>Optionally, one publication that you feel best illustrates your progress in your research.</li>
					<li>An email letter from your supervisor sent to cscw<?php echo $year; ?>doctoral@acm.org indicating that they support your application to the colloquium and that they agree that your research is at an appropriate stage for participation. These letters should also make reference to how you might benefit from the Doctoral Colloquium and how they expect that you might benefit the other students in this group experience. The email should have "CSCW Doctoral Colloquium: " + YOUR NAME in the subject header.</li>
				</ul>
			</p>
