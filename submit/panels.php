			<h3>What is a panel?</h3>
			<p>Panels are a great way to generate debate and raise new and interesting issues at CSCW. With panels we want to provide a forum for discussing provocative, controversial, innovative, emerging, boundary-spanning and boundary-breaking issues.</p>
			<p>Whereas paper sessions provide detailed discussions of work recently completed, panels provide an opportunity to explore what is on the horizon — or what is already here but not adequately recognized, acknowledged or discussed.
			</p>
			
			<h3>Criteria for Successful Panels</h3>
			<p>The best panels provide opportunities for audience participation. We especially encourage innovative formats that will:
				<ul>
					<li>Address relevant, critical and contemporary topics</li>
					<li>Engage the audience and provoke discussion</li>
					<li>Engage multiple disciplines in productive dialog</li>
				</ul>	
			</p>
			
		  	<h3>Submitting a Proposal</h3>
		  	<p>Panel proposals should be no longer than 4 pages in the SIGCHI Extended Abstracts format. They should include:
		  		<ul>
		  			<li>The panel topic</li>
		  			<li>Details of the panelists presented as one-paragraph biographical sketches describing their expertise</li>
		  			<li>The proposed structure and format of the panel, including how you intend to encourage interaction with the audience</li>
		  			<li>Technical requirements</li>
		  		</ul>
		  	</p>
		  	<p>Panel submissions should be e-mailed to the Panels Co-Chairs at panels2016@cscw.acm.org. All panel submissions are due by 5:00pm (17:00) Pacific Daylight Time on November 10, 2014.
		  	</p>
		  	
		  	<h3>Accepted Proposals</h3>
            <p>Accepted proposals will be published in the ACM Digital Library and distributed to attendees as part of the Conference Extended Abstracts.
            </p>
            <p>The final photo-ready abstract should be no longer than 4 pages and should include a description of the panel topic, one-paragraph biographical sketches of the panelists, a position statement by each panelist and an overview of the ways in which the position statements relate to each other. When a panel submission is accepted it is the panel organizer’s responsibility to receive confirmation from the panelists.
            </p>
            <p><strong>Please note that presenting panelists are responsible for their own conference registration fee, travel, and accommodation costs.</strong>
