			<h2>What is a panel?</h2>
			<p>A panel is a collection of people discussing an important topic to our field with energy, insight, and verve—they need not be dull and boring.</p>
			<p>Great panels are an excellent way to generate debate, raise new and interesting issues at CSCW, and hear multiple points of view on a given topic. CSCW panels are a forum for discussing provocative, controversial, innovative, emerging, boundaryspanning and boundary-breaking issues. 
			</p>
			<p>
			While paper sessions provide detailed discussions of work recently completed, panels provide an opportunity to hear from research leaders about what is on the horizon — or what is already here but not yet recognized, acknowledged or discussed. 
			</p>
			<h2>Criteria for Successful Panels</h2>
			<p>The best panels provide opportunities for audience participation. We especially encourage proposals for panels that will: 
				<ul>
					<li>Discuss relevant, important, and critical topics </li>
					<li>Engage the audience and provoke discussion </li>
					<li>Blend several disciplines in productive conversation </li>
				</ul>	
			</p>
			
		  	<h2>Submitting a Proposal</h2>
		  	<p>Panel proposals should be no longer than 4 pages in the <a href="http://sigchi.github.io/Document-Formats/" target="_blank">SIGCHI Extended Abstracts format</a>. They should include:
		  		<ul>
		  			<li>The panel topic</li>
		  			<li> One-paragraph biographical sketches of the panelists describing their expertise </li>
		  			<li>Proposed structure and format of the panel, including how you intend to encourage interaction with the audience</li>
		  			<li>Any technical requirements </li>
		  		</ul>
		  	</p>
