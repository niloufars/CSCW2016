			<strong>Abstract:</strong>Social signaling theory (SST) is an analytical approach for interpreting interactions in social settings that are not ostensibly explicit. Social computing researchers are beginning to apply SST to study questions of identity, trust, reliability, and multi-cultural communication. Example framing questions include:
			<ul>
			<li><em>Relationships</em>: How does social signaling theory complement existing theories used for studying social computing systems?
