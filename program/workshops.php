			<strong>Abstract:</strong> Emergencies are critical situations that demand immediate action to avoid adverse consequences to life and property. A key challenge in Emergency Management is decision-making under time pressure, with an overload of unconfirmed, uncertain and conflicting information, including the management of many people, with distinct and possibly fluid roles, in different places. Collaboration in these settings is an interesting element, since emergency response generally involves multiple agencies and the public, which have different views, protocols and priorities, but must act in concert to handle the situation. In addition, an increasing amount of virtual information is necessary to inform and manage volunteers. The goal of this workshop is to identify and map the main challenges of collaboration in crisis situations, review current research methods and approaches to address them and from there to address the lack of formal processes, structures, methodologies and tools, following a multidisciplinary approach.

