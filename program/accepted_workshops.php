                <strong>Abstract:</strong>
                <p>Location-based social network apps for dating have had explosive growth over the past few years. Although they have many possible uses, casual and sexual encounters remain an important part of their draw. For CSCW, these apps are interesting to study: they offer a context to explore issues of identity and self-presentation, geography and locality, privacy and security, as well as motivation and usage habits.
                </p>
                <p>In this one-day workshop, we invite researchers, students, and practitioners from a diverse range of backgrounds, including CSCW, computer science, sociology, and public health, to discuss these issues and more, as well as exploring the difficulties and challenges inherent in this research. In addition to exploring the issues around apps for sex and dating, participants will also help to bring some of these sensitive, yet important topics into the mainstream of CSCW research.
                </p>
                <p>Details of submissions for interested participants are available at the workshop website: <a href="http://www.markjhandel.com/index.php/CSCWSexApps" target="_blank">http://www.markjhandel.com/index.php/CSCWSexApps</a>.</p>
