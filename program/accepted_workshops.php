                <p>Saturday March 14</p>
                
                <strong>Organizers:</strong>
                <p>Amy Bruckman, Georgia Tech<br/>
                <span class="workshop_contact">Contact: </span><a href="mailto:casey.fiesler@gatech.edu">Casey Fiesler</a>, Georgia Tech<br/>
                Mary Gray, Microsoft Research<br/>
                Jeff Hancock, Cornell<br/>
                Wayne Lutters, UMBC<br/>
                Tamara Peyton, Penn State<br/>
                Alyson Young, UMBC<br/>
                </p>
                    
                <strong>Abstract:</strong>
                <p>The evolution of social technology and research methods present ongoing challenges to studying people online. Recent high-profile cases have prompted discussion among both the research community and the general public about the ethical implications of researching humans, their information, and their activities in large-scale digital contexts. Examples of scientific and market research involving Facebook users and OKCupid clients exemplify the ethical complexities of both studying and manipulating online user behavior. When does data science become human subjects research, and what are our obligations to these subjects as researchers? Drawing from previous work around the ethics of digital research, one goal of this workshop is to work towards a set of guiding principles for CSCW scholars doing research online.
                </p>
