                <h4 id="W6">W6. Advancing an industry/academic partnership model for Open Collaboration research</h4>	
                <p>Saturday March 14</p>
                
                <strong>Organizers:</strong>
                <p><span class="workshop_contact">Contact: </span><a href="mailto:jmorgan@wikimedia.org">Jonathan Morgan</a>, Wikimedia Foundation<br/>
                Aaron Halfaker, Wikimedia Foundation<br/>
                Dario Taraborelli, Wikimedia Foundation<br/>
                Tim Hwang, Imgur<br/>
                Sean Goggins, University of Missouri<br/>
                </p>
                    
                <strong>Abstract:</strong>
                <p>Open collaboration systems like Wikipedia, Imgur, Zooniverse, StackExchange, and Reddit have shown that networked communities of volunteer contributors can produce and maintain immense, valuable public resources. A growing body of work within the CSCW research community has come to recognize novel opportunities and challenges that openness brings with it. For example: </p>
                <ul>
                    <li>Distributed production and the need for quality control</li>
                    <li>Maintaining volunteer motivation to participate</li>
                    <li>Distributing the processes for articulation and governance</li>
                    <li>Community maintenance via socializing newcomers</li>
                </ul>
                <p>However, findings from these academic studies do not always permeate the boundaries between scholarship and practice. Furthermore, many important phenomena related to peer production are not yet fully understood. This workshop will focus on characterizing areas of research within the domain of Open Collaboration Systems (OCS’s) where partnerships between academic and industry researchers can both increase our scientific understanding of OCS’s and also support those systems through research. This workshop's purpose is to bring together researchers on both sides of the "data divide" to identify current challenges and opportunities for future research within these research areas, and to develop a preliminary set of requirements for improved resource sharing and collaboration between OC enterprises and academic research institutions.</p>

                <p>For more information and to apply see: <a href="https://meta.wikimedia.org/wiki/Research:CSCW15_workshop" target="_blank">https://meta.wikimedia.org/wiki/Research:CSCW15_workshop</a>.
                </p>
