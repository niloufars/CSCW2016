                <p>Saturday March 14</p>
                
                <strong>Organizers:</strong>
                <p>
                <span class="workshop_contact">Contact: </span><a href="mailto:depaula@acm.org">Rogério de Paula</a>, IBM Research<br/>
                Marcos R S Borges, PPGI/Federal University of Rio de Janeiro<br/>
                Cleidson R. B. de Souza, Vale Institute of Technology and UFPA<br/>
                David Randall, University of Siegen<br/>
                David Millen, IBM Research<br/>
                </p>
                    
                <strong>Abstract:</strong>
                <p>The workshop organizers are members of an informal committee currently planning CSCW 2017 in Brazil. As we get set for this event, it is paramount to enroll a broader number of CSCW researchers in Latin America (LA) in the broader CSCW community. CSCW research in LA has been active since the mid 1990’s although largely disconnected from the broader international CSCW. It is critical to acknowledge this history and collectively understand how the CSCW community can learn from CSCW in LA and how LA researchers can be better represented in and integrated into international CSCW. The goal of this workshop is twofold. First, to provide a forum for researchers working in LA to present their work for the international CSCW community, and second, to allow the international CSCW community to understand the issues around doing CSCW research in LA. Senior discussants will be invited to discuss the work of LA researchers and introduce key issues from their regions, therefore facilitating the cross-region conversations among participants.
                </p>
