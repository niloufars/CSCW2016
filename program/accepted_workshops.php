                <h4 id="W1">W1. The Future of Networked Privacy: Challenges and Opportunities</h4>	
                <p>Saturday March 14 & Sunday March 15</p>
                
                <strong>Organizers:</strong>
                <p><strong>Contact: </strong><a href="mailto:jvitak@umd.edu">Jessica Vitak</a>, College of Information Studies, University of Maryland<br/>
                Pamela Wisniewski, College of Information Sciences and Technology, Pennsylvania State University<br/>
                Xinru Page, School of Information and Computer Sciences, University of California-Irvine<br/>
                Airi Lampinen, Mobile Life Centre, Stockholm University<br/>
                Eden Litt, Media, Technology & Society, Northwestern University<br/>
                Ralf de Wolf, iMinds-SMIT, Brussels<br/>
                Patrick Gage Kelley, Computer Science Department, University of New Mexico<br/>
                Manya Sleeper, School of Computer Science, Carnegie Mellon University<br/>
                </p>
                    
                <strong>Abstract:</strong>
                <p>Building on recent research in privacy management and disclosure in networked spaces, this two-day workshop examines networked privacy challenges from a broader perspective by (1) identifying the most important issues researchers will need to address in the next decade and (2) working to create actionable solutions for these privacy issues. This workshop comes at a critical time for organizations, researchers, and consumers, as content-sharing applications soar in popularity and more privacy and security vulnerabilities emerge. Workshop participants and organizers will work together to develop a guiding framework for the community that highlights the future challenges and opportunities of networked privacy.
                </p>
                <p>To participate in the workshop, submission and acceptance of a position paper is required. For details on the workshop, including topics of interest and the preliminary schedule, visit: </br><a href="http://networkedprivacy2015.wordpress.com" target="_blank">http://networkedprivacy2015.wordpress.com</a>.
                </p>
