                <p>Saturday March 14 & Sunday March 15</p>
                
                <strong>Organizers:</strong>
                <p>Elizabeth Daly, IBM Research Lab Dublin<br/>
                <span class="workshop_contact">Contact: </span><a href="mailto:serete@cdm.depaul.eduk">Sheena Erte</a>, DePaul University<br/>
                <span class="workshop_contact">Contact: </span><a href="mailto:rfarzan@pitt.edu">Rosta Farzan</a>, University of Pittsburgh<br/>
