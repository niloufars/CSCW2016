                Daniele Quercia, Yahoo! Labs Barcelona<br/>
                Raz Schwartz, Cornell Tech NYC<br/>
                Amy Voida, Indiana University Indianapolis
                </p>
                    
                <strong>Abstract:</strong>
                <p>Challenges of local communities such as encouraging civic engagement and facilitating collaboration to address local issues have motivated researchers and practitioners to explore the role of technologies in the city life. The goal of this workshop is to open a discussion on how to design, build and study technology infrastructure to support cities, neighborhoods and local communities. We aim to create a publicly accessible repository of infrastructure tools and to facilitate the cross-pollination of ideas about technology in local contexts among the researchers, practitioners and residents interested in this area. At the workshop, participants will collaborate with Vancouver’s residents and technology practitioners in order to explore the past, present and future of research in this space, co-construct the infrastructuring tools repository, discuss key information challenges of local communities and brainstorm solutions and opportunities to address them. Discussion and ideas generated will be archived online to be available to the larger research community.
                </p>
