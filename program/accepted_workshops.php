                <h4 id="W2">W2. Moving Beyond e-Health and the Quantified Self</h4>	
                <p>Saturday March 14 & Sunday March 15</p>
                
                <strong>Organizers:</strong>
                <p>Alan Chamberlain, University of Nottingham<br/>
                <span class="workshop_contact">Contact: </span><a href="mailto:mc@ecs.soton.ac.uk">mc schraefel</a>, University of Southampton<br/>
                Erika Poole, Penn State<br/>
                Sean Munson, University of Washington<br/>
                Catalina Danis, IBM Watson<br/>
                Elizabeth Churchill, Google
                </p>
                    
                <strong>Abstract:</strong>
                <p>In this workshop, we propose to situate health, first as a proactive (de-medicalised) experience that facilitates innovation, creativity and improved quality of life; second, we seek specifically to use this workshop to explore how CSCW methods and design experience may inform such an exploration to support interactive technology from social/industrial collaborative infrastructure to individual/social interactive systems for what we call Wellth Creation, that is sustainable health and wellbeing IT that is Proactive and Aspirtational.
                </p>
                <p>We ask, what is the role of CSCW as methodology and epistemology in the development of interactive technology for Proactive Health & Wellbing? What contributions are CSCW researchers best prepared make to the critical and timely development of re-designing our cultures to support health as a social good? This workshop proposes to dedicate its two days to exploring these questions and to produce a research agenda for CSCW for Proactive Health. These questions are both critical and timely.
                </p>
                <p>Website and full workshop outline: <a href="http://cscw2015.tumblr.com/about" target="_blank">http://cscw2015.tumblr.com/about</a></p>
