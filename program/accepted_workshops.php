                <h4 id="W10">W10. Connecting Collaborative & Crowd Work with Online Education</h4>	
                <p>Sunday March 15</p>
                
                <strong>Organizers:</strong>
                <p><span class="workshop_contact">Contact: </span><a href="mailto:josephjaywilliams@stanford.edu">Joseph Jay Williams</a>, HarvardX<br/>
                Markus Krause, Leibniz University<br/>
                Praveen Paritosh, Google<br/>
                Jacob Whitehill, HarvardX<br/>
                Justin Reich,HarvardX<br/>
                Juho Kim, MIT<br/>
                Piotr Mitros, edX<br/>
