                <div class="accepted_workshop">
                <h4 id="W11">W11. Supporting “Local Remote” Collaboration: Applying and Adapting Remote Group Awareness Techniques to Adapting Remote Group Awareness Techniques to Co-located Settings</h4>	
                <p>Sunday March 15</p>
                
                <strong>Organizers:</strong>
                <p><span class="workshop_contact">Contact: </span><a href="mailto:stacey.scott@uwaterloo.ca">Stacey Scott</a>, University of Waterloo<br/>
                T.C. Nicholas Graham, Queen's University<br/>
                James R. Wallace, University of Waterloo<br/>
                Mark Hancock, University of Waterloo<br/>
                Miguel Nacenta, University of St. Andrews
                </p>
                    
                <strong>Abstract:</strong>
                <p>Co-located environments are often seen as ideal group work settings since they provide a rich communication environment (e.g. delay-free voice communication, face-to-face interaction, eye gaze, and non-verbal communication) that promotes awareness and coordination. However, multi-device ecologies, in which co-located collaborators utilize multiple personal devices (e.g., laptops, tablets) or personal devices in conjunction with large shared displays, such as digital walls or tabletops, can interfere with common face-to-face communication and collaboration strategies. These contexts shift group members’ focus, at least some of the time, toward their personal devices and away from their collaborators or any shared artifacts. Group communication and coordination can easily breakdown in these scenarios due to the lack of shared group focus. This workshop will explore design techniques to address this issue, and improve group awareness in these co-located multi-device ecologies. This will be accomplished through group presentations, brainstorming sessions, and small-group breakout sessions.   
                </p>
                <p>Workshop website: <a href="http://localremote.wordpress.com/" target="_blank">http://localremote.wordpress.com/</a></p>
