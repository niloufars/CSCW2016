          <p>Remote attendance will be available during the conference workshops and doctoral consortium (Saturday and Sunday), the main talk sessions (Monday to Wednesday), and possibly the conference reception.</p>    
          <p>Selected remote attendees can either: 
          <ol>
            <li> Participate in the entire conference and have a Beam dedicated to them throughout</li>
              <li> Time-share a Beam with other remote attendees where you will select particular sessions you want to attend</li>
          </ol>
          </p>   
          <p>For both options, we are seeking people who can commit to attending a large portion of the conference. We will not be accepting applications for those who want to connect into a single session.</p>
          <p>Prior to the conference, you will provide you with <strong>technical training</strong> on how to use the Beam, and <strong>social training</strong> on how to use it a public conference setting with other attendees.</p>
      

          <h1>How Can I Participate Remotely</h1>

          <p>If you are interested in attending CSCW remotely, please fill out <a href="https://docs.google.com/forms/d/1wzBZ6OGBVyot0a9QOwiZJYgAdfEIoYYwy3IYLpKByVU/viewform">this application form</a> by December 1, 2015: </p>

          <p>We will be selective about who we allow to participate remotely.  We aim to have remote attendees from varying backgrounds so we can explore the experience from different perspectives, while also providing a valuable option of attending from afar.  We will also be selective about who receives a dedicated Beam and who time-shares a Beam.</p>

          <p>If you are selected to participate remotely, we will contact you by January 2, 2016 (approximately one week before the early registration deadline).  Because remote attendance is still in an exploratory and learning stage, you are likely to experience technology weaknesses and glitches. As a result, your registration cost will be half the normal rate.</p>

          <h1>The User Study</h1>
          <p>As part of the telepresence and remote participation, we will be conducting a user study to learn about the experience.  If you are selected to attend remotely, you will be asked to participate in this study.  The study will involve:
          </p>
          <ol>
            <li>A pre-conference interview with you to discuss your pre-conceptions about the experience prior to the conference as well as your goals for the conference.
            </li>
            <li>Completion of a diary at the end of each conference day that describes your thoughts and experiences.
            </li>
            <li>A final interview at the end of the conference to learn about your overall experience and feelings towards each of the telepresence options.
            </li>

          </ol>
          <p>We will also conduct an online survey with conference attendees who were present in person to gauge their reactions to the telepresence setup.  A similar study was conducted at Ubicomp and ISWC 2014 â€“ you can find a publication on it <a href="http://clab.iat.sfu.ca/pubs/Neustaedter-Telepresence-CSCW2016.pdf">here</a>.  The paper describes what you might expect from the telepresence experience at CSCW, though some aspects may differ.
