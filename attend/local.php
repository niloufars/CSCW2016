					href="http://www.sfcablecar.com/">http://www.sfcablecar.com/</a>)
				</li>
				<li>Chinatown - The largest Chinatown outside of Asia and the oldest
					in North America is best explored on foot. Eat some delicious
					Chinese food and explore the many stores (<a
					href="http://www.sanfranciscochinatown.com">http://www.sanfranciscochinatown.com</a>)
				</li>
				<li>Ghirardelli Square - Originally a chocolate factory, this square
					now hosts a variety of waterfront stores and restaurants and, of
					course, some delicious chocolate and ice cream! (<a
					href="http://www.ghirardellisq.com">http://www.ghirardellisq.com</a>).
				</li>
				<li>Japanese Tea Garden - Don&rsquo;t miss the oldest public
					Japanese Garden in the U.S., which is also where the fortune cookie
					was introduced to Americans. Enjoy the serene atmosphere while you
					sip some tea in their tea house (<a
					href="http://japaneseteagardensf.com">http://japaneseteagardensf.com</a>)
				</li>
			</ul>

			<h2>Restaurants</h2>
			<ul>
				<li>Aziza/Mourad - Chef Mourad Lahlou&rsquo;s restaurants serve
