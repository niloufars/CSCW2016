				</li>
				<li>Golden Gate Bridge - Iconic Golden Gate Bridge connects San
					Francisco to Marin County. It&rsquo;s scenic to visit from the SF
					side, and you can walk or bike onto or across it, even going so far
					as Sausalito for lunch. Bikes are available for rent all over San
					Francisco. (<a href="http://goldengatebridge.org/visitors/">http://goldengatebridge.org/visitors/</a>)
				</li>
				<li>Coit Tower - Located in Telegraph Hill, walking distance from
					the conference hotel, the 1922 Art Deco tower and its art make an
					easy and beautiful outing from the conference. (<a
					href="http://sfrecpark.org/destination/telegraph-hill-pioneer-park/coit-tower/">http://sfrecpark.org/destination/telegraph-hill-pioneer-park/coit-tower/</a>)
				</li>
				<li>Alcatraz - You need to take a ferry to Alcatraz, which is a bit
					pricy, but in our opinion quite worthwhile if you have the time and
					interest; it is a visually and emotionally captivating excursion.
					Ferries are available at Pier 33. (<a
					href="http://www.nps.gov/alca/index.htm">http://www.nps.gov/alca/index.htm</a>)
				</li>
				<li>Cable cars - Hang off the side of one of San Francisco&rsquo;s
					iconic cable cars. You can catch them from a variety of stations
