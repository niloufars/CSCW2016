			<h2>Nature &amp; Exercise</h2>
			<ul>
				<li>Golden Gate Park - Golden Gate Park has tons to do whether
					you&rsquo;re up for a quick visit or a longer stay. Lots of trails
					for hiking and biking, botanical gardens, the California Academy of
					Sciences, and the De Young museum, just to name a few (there is
					even a herd of bison that live in the park!). To get there grab the
					N-Judah Muni train from Embarcadero, right across from the
					conference hotel. (<a
