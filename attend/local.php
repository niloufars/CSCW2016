					in the world (<a href="http://www.asianart.org">http://www.asianart.org</a>)
				</li>
				<li>California Academy of Sciences - Part zoo, part natural history
					museum, part planetarium, part aquarium, there&rsquo;s an awful lot
					to see here! Go early if you go on the weekend. (<a
					href="http://www.calacademy.org/">http://www.calacademy.org/</a>)
				</li>
				<li>Museum of the African Diaspora: Explore the history, art, and
					culture of the African Diaspora (<a href="http://www.moadsf.org">http://www.moadsf.org</a>)
				</li>
