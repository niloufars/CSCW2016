				<li>East Bay Parks - The hills above Berkeley and Oakland are a
					continuous string of parks. Hike for miles and witness stunning
					views of the bay starting in Tilden park which is accessible and
					wonderful above Berkeley. (<a
					href="http://www.ebparks.org/parks/tilden.htm">http://www.ebparks.org/parks/tilden.htm</a>)
				</li>
				<li>Bay Trail: Enjoy walking or riding along this ~340 mile trail
					along the Bay. (<a href="http://baytrail.org">http://baytrail.org</a>)
				</li>
				<li>The Presidio - If you&rsquo;re seeking nature, head here to
					enjoy majestic woods and stunning views of the Golden Gate Bridge
					and the Bay. (<a href="http://www.nps.gov/prsf/index.htm">http://www.nps.gov/prsf/index.htm</a>)
				</li>
			</ul>
			
			<h2>Museums</h2>
			<ul>
				<li>De Young - In Golden Gate Park, museum with a view. (<a
