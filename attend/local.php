				</li>
				<li>Chez Panisse - Alice Waters&rsquo; famed Berkeley eatery is
					worth a trek (the BART train is a good way to get there).
					Reservations are hard to come by, but plan in advance either for
					the upstairs casual bistro, or the much fancier (and pricier) pre
					fixe downstairs dining room. (<a href="http://www.chezpanisse.com/">http://www.chezpanisse.com</a>)
				</li>
				<li>Dosa on Valencia - South Indian fare with a California take. Get
